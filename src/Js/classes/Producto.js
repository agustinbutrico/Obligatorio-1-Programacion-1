class Producto {
  constructor(_id, _nombre, _precio, _descripcion, _imagen, _stock, _estado, _oferta, _cantUnidades) {
    this.id = _id;
    this.nombre = _nombre;
    this.precio = _precio;
    this.descripcion = _descripcion;
    this.imagen = _imagen;
    this.stock = _stock;
    this.estado = _estado;
    this.oferta = _oferta;
    this.cantUnidades = _cantUnidades;
  }
}
